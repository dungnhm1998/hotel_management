    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        //User u = new User();
        Customer c = new Customer();
        c.setName(txtCustomerName.getText());
        c.setGender(Boolean.parseBoolean(txtGender.getText()));
        c.setDateOfBirth(txtDateOfBirth.getText());
        c.setAddress(txtAddress.getText());
        c.setPhoneNumber(txtPhoneNumber.getText());
        c.setIndentityNumber(txtIndentityNumber.getText());
        if (new CustomerDAO().addCustomer(c)) {
            listCustomer.add(c);
            JOptionPane.showMessageDialog(rootPane, "Add success");
            showResult();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Add fail");
        }

    }//GEN-LAST:event_btnAddActionPerformed

