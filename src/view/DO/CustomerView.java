    public void ShowTable() {
        
        listCustomer = new CustomerDAO().getListCustomer();
        for (Customer c : listCustomer) {
            String gender = "nam";
            if (c.getGender() == false) {
                gender = "nu";
            }
            model2.addRow(new Object[]{
                c.getCustomerID(), c.getName(), gender, c.getDateOfBirth(),
                c.getAddress(), c.getPhoneNumber(), c.getIndentityNumber()
            });
        }
    }

