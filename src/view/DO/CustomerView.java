    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        model2.setRowCount(0);
        String customerName = "";
        String gender = "nam";
        if (txtCustomerName.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Chua nhap ten ma bat tim");
        } else {
            customerName = txtCustomerName.getText();
            for (Customer c : listCustomer) {
                if (c.getName().equals(customerName)) {
                    if (c.getGender() == false) {
                        gender = "nu";
                    }
                    model2.addRow(new Object[]{
                        c.getCustomerID(), c.getName(), gender, c.getDateOfBirth(),
                        c.getAddress(), c.getPhoneNumber(), c.getIndentityNumber()
                    });
                    break;
                }
            }
            if (customerName.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Khong tim thay ten nay");
            }
        }
    }//GEN-LAST:event_btnFindActionPerformed

    public void showResult() {
        Customer c = listCustomer.get(listCustomer.size() - 1);
        String sex = "";
        if (c.getGender() == true) {
            sex = "nam";
        } else {
            sex = "nu";
        }
        model2.addRow(new Object[]{
            c.getCustomerID(), c.getName(), sex,
            c.getDateOfBirth(), c.getAddress(), c.getPhoneNumber(), c.getIndentityNumber()
        });
        //model2.fireTableDataChanged();
    }

